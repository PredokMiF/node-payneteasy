"use strict";

function smartSpaceSplit (str) {
    var start, i, l = str.length, ch, out = [];

    for (start = 0; str[start] === ' '; start++);
    i = start;

    while (i < l) {
        ch = str[i];
        if (ch === '\'' || ch === '"' || ch === '/') {
            return false;
        }
        if (ch === ' ') {
            if (start !== i) {
                out.push(str.substring(start, i));
            }
            start = ++i;
        } else {
            i++;
        }

    }

    if (start !== i) {
        out.push(str.substring(start, i));
    }

    return out;
}

module.exports = smartSpaceSplit;