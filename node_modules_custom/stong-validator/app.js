"use strict";

var parser = require('./parsers/parser');
var executor = require('./executor/executor');

function stongValidator (tpl, cfg) {
    var def = parser(tpl, cfg);

    return function (obj) {
        return def.then(
            function (tpl, cfg) {
                return executor('', tpl, obj, cfg);
            }
        );
    };
}

module.exports = stongValidator;